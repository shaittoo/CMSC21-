int i,j; 		//declares integer variables i and j
float x,y; 		 //declares float variables x and y

i = 10; 		//assign the value of i to 10
j = 20;     	//assign the value of j to 20
x = 43.2892f;   //assign the value of x to 43.2892 as a float
y = 5527.0f;    //assign the value of y to 5527.0 as a float

printf("i = %d, j = %d, x = %f, y = %f\n", i, j, x, y); //displays the values of i, j, x, and y
														//with %d specifier as integer and %f specifier as float